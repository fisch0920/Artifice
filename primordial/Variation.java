package primordial;
/**
 * Enumeration for different drawing types of 
 * Primordial Organisms
 * 
 * @author Travis Fischer (tfischer)
 * @date 12/25/2006
 */

public enum Variation {
    // Transparent organisms look similar to those viewed 
    // in a standard petri dish
    PRIMORDIAL_DEFAULT, 
    
    // Reversed organisms which aggregately draw themselves 
    // upon a canvas producing a snakeskin-like backdrop
    PRIMORDIAL_SNAKESKIN, 
}
